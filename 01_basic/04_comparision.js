/*
> : Greater than
>= : Greater than or equal to
< : Less than
<= : Less than or equal to
== : Equal to
!= : Not equal to
=== : Equal value and equal datatype
*/
console.log(10>5) //output: true
console.log(10>=5) //output: true
console.log(10<5) //output: false
console.log(10<=5) //output: false
console.log(10==5) //output: false
console.log(10!=5) //output: true
console.log(10===5) //output: false
console.log(10===10) //output: true
console.log(10==="10") //output: false